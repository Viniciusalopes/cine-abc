package dao;

/**
 *
 * @author vovostudio
 */
public class Pessoa {

    private int id;
    private String nome;
    private char sexo;
    private String email;
    private String cpf;

    public Pessoa(String nome, char sexo, String email, String cpf) {
        this.nome = nome;
        this.sexo = sexo;
        this.email = email;
        this.cpf = cpf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

}
